// Helper function to get Arabic text for a specific ayah
export const getArabicText = (surahNumber, ayahNumber) => {
  const surah = quranText[surahNumber];
  if (!surah) return null;
  return surah[ayahNumber] || null;
};

// Helper function to get next N ayahs
export const getNextAyahs = (surahNumber, startAyah, count = 3) => {
  const surah = quranText[surahNumber];
  if (!surah) return [];
  
  const nextAyahs = [];
  for (let i = 0; i < count; i++) {
    const ayahNumber = startAyah + i;
    const text = surah[ayahNumber];
    if (text) {
      nextAyahs.push({
        ayah: ayahNumber,
        text: text,
        available: true
      });
    } else {
      // Add placeholder for missing ayahs
      nextAyahs.push({
        ayah: ayahNumber,
        text: `[Ayah ${ayahNumber} not available in sample data]`,
        available: false
      });
    }
  }
  return nextAyahs;
};

// Helper function to check if ayah exists
export const ayahExists = (surahNumber, ayahNumber) => {
  const surah = quranText[surahNumber];
  return surah && surah[ayahNumber];
};
